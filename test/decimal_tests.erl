    Tests =
        [
         {"1", {1, 0}},
         {"-1", {-1, 0}}
        ],
    [
     {V, fun() -> R = decimal:from_list(V) end}
     || {V, R} <- Tests
    ].

binary_test_() ->
    PosTests =
        [
         {<<"1">>, {1, 0}},
         {<<"123">>, {123, 0}},
         {<<"1230">>, {123, 1}},
         {<<"1.23">>, {123, -2}},
         {<<"1.2300">>, {123, -2}},
         {<<"1e0">>, {1, 0}},
         {<<"12e1">>, {12, 1}},
         {<<"12300e3">>, {123, 5}},
         {<<"1.2300e0">>, {123, -2}},
         {<<"1.2300e2">>, {123, 0}},
         {<<"1.2300e-2">>, {123, -4}},
         {<<"123.45600e-2">>, {123456, -5}},
         {<<"1e-0">>, {1, 0}},
         {<<"12e-1">>, {12, -1}},
         {<<"12300e-3">>, {123, -1}},
         {<<"1e+0">>, {1, 0}},
         {<<"12e+1">>, {12, 1}},
         {<<"12300e+3">>, {123, 5}}
        ],
    NegTests = [{<<$-, V/binary>>, {-B, E}} || {V, {B, E}} <- PosTests],
    [
     {V, fun() -> R = decimal:normalize(decimal:from_binary(V)) end}
     || {V, R} <- PosTests ++ NegTests
    ].

