         { {0,0}, {1,0},  -1 },

         { {0, -100}, {0, 100}, 0},
         { {10, 0}, {1, 1}, 0 },
         { {123, -2}, {122, -2}, 1 },
         { {-123, -2}, {-122, -2}, -1 }
        ],
    [
     {iolist_to_binary(
          [
           decimal:to_binary(A),
           case R of
               1 -> $>;
               0 -> $=;
               -1 -> $<
           end,
           decimal:to_binary(B)
          ]),
      fun() ->
          R = decimal:cmp(A, B)
      end} || {A, B, R} <- Tests
    ].

