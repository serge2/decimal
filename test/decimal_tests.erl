divide_test_() ->
    Tests =
        [
         {<<"1">>, <<"2">>, <<"0.5">>},
         {<<"1">>, <<"-2">>, <<"-0.5">>},
         {<<"1">>, <<"0.2">>, <<"5.0">>},
         {<<"1e3">>, <<"1e-3">>, <<"1.0e6">>},
         {<<"3e-1">>, <<"3e-1">>, <<"1.0">>},
         {<<"1">>, <<"3">>, <<"0.", (repeate($3, 100))/binary>>},
         {<<"2">>, <<"3">>, <<"0.", (repeate($6, 99))/binary, "7">>},
         {<<"1">>, <<"0">>, {error, badarith}}
        ],
    [
     {<<A/binary, $/, B/binary>>,
      fun() ->
          A1 = decimal:from_binary(A),
          B1 = decimal:from_binary(B),
          R = try
                  decimal:to_binary(decimal:normalize(decimal:round(half_up, decimal:divide(A1, B1, 100), 100)), #{pretty => true})
              catch
                  Err:Res -> {Err,Res}
              end
      end}
     || {A, B, R} <- Tests
    ].

repeate(Ch, N) ->
    << <<Ch>> || _ <- lists:seq(1, N)>>.

neg_test() ->
    ?assertEqual({-1, 0}, decimal:neg({1,0})),
    ?assertEqual({1, 0}, decimal:neg({-1,0})).

abs_test() ->
    ?assertEqual({1, 0}, decimal:abs({1,0})),
    ?assertEqual({1, 0}, decimal:abs({-1,0})).

